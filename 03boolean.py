word1 = "Cat"

word2 = "Dog"

if word1 < word2:
    word1first = True
else:
    word1first = False
    
if word1first:
    print(word1 + " comes before " + word2  + ' ' " alphabetically " )
else:
    print(word1 + " comes after " + word2 + ' ' "alphabetically " )








